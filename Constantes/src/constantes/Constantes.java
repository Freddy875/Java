public class Constantes {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int iNumero = 10; //Esto es un variable
        
        System.out.println("El valor del entero es: " + iNumero);
                
        iNumero = 15;
        
        System.out.println("Ahora el valor del entero es: " + iNumero);
        
        final int iConstante = 10; //Eso es una constante
        
        //iContante = 15; No se puede cambiar una constante
        
        System.out.println("El valor de la constante es: " + iConstante);
        
        
             
        
    }//fin public static void main
    
}
